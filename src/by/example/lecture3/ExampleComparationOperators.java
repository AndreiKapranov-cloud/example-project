package by.example.lecture3;

public class ExampleComparationOperators {

    public static void main(String[] args) {

        int a = 17;
        int b = 21;

        System.out.println("a == b = " + (a == b)); // false
        System.out.println("a != b = " + (a != b)); // true

        System.out.println("a > b = " + (a > b)); // false
        System.out.println("a < b = " + (a < b)); // true

        System.out.println("a >= b = " + (a >= b)); // false
        System.out.println("a <= b = " + (a <= b)); // true

    }

}
